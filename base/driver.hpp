/** @defgroup driver  Problem driver for more convenient coding
 *
 *  Driver classes encapsulate specific, recurrent problems in order to
 *  - reduce reduncies
 *  - provided convenience to the user
 *
 *  Most problems addressed by the library relate to Poisson equation,
 *  diffusion equations, compressible hyperelasticity or Stokes flow.
 *  For these problems, specific driver classes can be used which reduce
 *  significantly the size of the application which is based on them.
 *  By design, the drivers allow the user to either use them in a black-box
 *  fashion or work with a part of the functionality only. 
 */
